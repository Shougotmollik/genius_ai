class SupplierProduct {
  final String title;
  final String supplierName;
  final String status;
  final String quality;
  final String purchaseDate;
  final String price;

  SupplierProduct({
    required this.title,
    required this.supplierName,
    required this.status,
    required this.quality,
    required this.purchaseDate,
    required this.price,
  });
}
